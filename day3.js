let lines = input.split('\n');
const LINE_LEN = lines[0].length;

const doTheTravel = ([xinc, yinc]) => {
    let travelPosX = 0;
    let hitTrees = 0;
    for (line = 0; line< lines.length; line=line+yinc) {
        let travelLine = lines[line];
        let pos = travelPosX % LINE_LEN;
        let replaceChar = 'O'
        if (travelLine[pos] === '#') {
            replaceChar = 'X';
            hitTrees++;
        } 
        travelPosX += xinc;
    }
    return hitTrees;
}

const doManyTravels = () => {
    /*
    Right 1, down 1.
    Right 3, down 1. (This is the slope you already checked.)
    Right 5, down 1.
    Right 7, down 1.
    Right 1, down 2.
    */
    let travel = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]];
    let travelTrees = travel.map(doTheTravel);
    console.log(travelTrees);
    return travelTrees.reduce((prev, current) => prev * current, 1);
}
